function smallLifeCyclePie(fund, divID, startingData) {

  if (startingData) {
    var myChart = Highcharts.chart(divID, {
          credits: { enabled: false },
          chart: {
              type: 'pie',
              backgroundColor: 'transparent',
              plotBackgroundColor: null,
              plotBorderWidth: 1, // null,
              plotShadow: false,
              styledMode: true
          },
          title: {
              text: null
          },
          tooltip: {
            formatter: function () {
                var rc = '<div class="before-invest-tooltip">'
                        + '<span class="legend-title"><strong>'+this.series.points[0].name+'</strong></span><br>';
                for (i = 1; i <= 5; i++) {
                  rc += '<span class="'+this.series.points[i].colorIndex+'-fund-color legend-left">'
                        + this.series.points[i].name + '</span>'
                        + '<span class="legend-right">' + this.series.points[i].y.toFixed(2)
                        + '%</span><br>';
                }
                rc += '</div>';
                return rc;
            },
            shared: true,
            useHTML: true,
            positioner: function () { return { x: 0, y: 77 }; }
          },
          plotOptions: {
              series: { states: { hover: { enabled: false } } },
              pie: {
                  allowPointSelect: false,
                  // cursor: 'pointer',
                  dataLabels: {
                      enabled: false,
                  },
  		              showInLegend: false
              }
          },
          series: [{
              startAngle: 90,
              data: startingData
          }]
      });

    return myChart;
  }
  return null;
}
